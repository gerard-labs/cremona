#!/usr/bin/env node
/**
 * tools/gen-skill-catalog.js — regenerate the Cremona skill's component catalog.
 *
 * Walks src/templates/components/, and for every component reads:
 *   - the one-line description from its <slug>.html.twig header comment,
 *   - its ring from the <slug>.story.vue `group="Ring N"` attribute,
 *   - whether it is interactive (the twig carries a `data-controller`).
 *
 * Emits .claude/skills/cremona-ui/catalog.md — the inventory an AI agent
 * consults before hand-writing UI. Re-run after adding/removing components:
 *   pnpm skill:catalog
 */

import { readFileSync, writeFileSync, readdirSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { resolve, dirname, join } from 'node:path';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const COMPONENTS_DIR = join(ROOT, 'src/templates/components');
const OUT = join(ROOT, '.claude/skills/cremona-ui/catalog.md');

const RING_NAMES = {
  1: 'Ring 1 — Primitives',
  2: 'Ring 2 — Compounds',
  3: 'Ring 3 — Patterns',
};

/**
 * First sentence of the `Name — description…` paragraph in the twig's
 * header comment (the paragraph wraps across lines, ends at a blank line).
 */
function describe(twig) {
  const block = twig.match(/\{#-?([\s\S]*?)-?#\}/);
  const lines = (block ? block[1] : twig.slice(0, 2000))
    .split('\n')
    .map((l) => l.trim());

  let i = lines.findIndex((l) => l.includes(' — '));
  if (i === -1) return '';

  const buf = [];
  for (; i < lines.length && lines[i] !== ''; i++) buf.push(lines[i]);
  let text = buf.join(' ').replace(/\s+/g, ' ').trim();

  const dash = text.indexOf(' — ');
  if (dash !== -1) text = text.slice(dash + 3);

  const period = text.indexOf('. ');
  if (period !== -1) text = text.slice(0, period + 1);
  text = text.replace(/\s*\(Ring \d\)\s*\.?\s*$/, '').trim();
  if (text && !/[.!?]$/.test(text)) text += '.';
  return text;
}

const components = [];
for (const slug of readdirSync(COMPONENTS_DIR).sort()) {
  const dir = join(COMPONENTS_DIR, slug);
  const twigPath = join(dir, `${slug}.html.twig`);
  const storyPath = join(dir, `${slug}.story.vue`);
  if (!existsSync(twigPath)) continue;

  const twig = readFileSync(twigPath, 'utf8');
  const story = existsSync(storyPath) ? readFileSync(storyPath, 'utf8') : '';

  const ring = Number((story.match(/group="Ring (\d)"/) || [])[1] || 0);
  const ctrl = (twig.match(/data-controller="([^"]+)"/) || [])[1] || '';

  components.push({
    slug,
    internal: slug.startsWith('_'),
    ring,
    description: describe(twig),
    controllers: ctrl ? [...new Set(ctrl.split(/\s+/))].join(', ') : '',
  });
}

const lines = [];
lines.push('# Cremona component catalog');
lines.push('');
lines.push(
  `_Generated by \`tools/gen-skill-catalog.js\` — do not edit by hand. ` +
    `${components.filter((c) => !c.internal).length} components._`,
);
lines.push('');
lines.push(
  'Every component lives at `src/templates/components/<slug>/` — a ' +
    '`<slug>.html.twig` (canonical markup, the source of truth) and a ' +
    '`<slug>.story.vue` (live demo). Components flagged **[JS]** ship a ' +
    'Stimulus controller — copy the `data-controller` / `data-action` / ' +
    '`data-*` attributes from the twig verbatim or they will be inert.',
);
lines.push('');

for (const ring of [1, 2, 3]) {
  const inRing = components.filter((c) => c.ring === ring && !c.internal);
  if (!inRing.length) continue;
  lines.push(`## ${RING_NAMES[ring]}`);
  lines.push('');
  for (const c of inRing) {
    const js = c.controllers ? ` **[JS: ${c.controllers}]**` : '';
    lines.push(`- \`${c.slug}\` — ${c.description}${js}`);
  }
  lines.push('');
}

const internal = components.filter((c) => c.internal);
if (internal.length) {
  lines.push('## Internal partials');
  lines.push('');
  lines.push('_Shared shells used by other components — not consumed directly._');
  lines.push('');
  for (const c of internal) lines.push(`- \`${c.slug}\` — ${c.description}`);
  lines.push('');
}

writeFileSync(OUT, lines.join('\n'));
console.log(
  `[gen-skill-catalog] wrote ${OUT} — ${components.length} components ` +
    `(${components.filter((c) => c.controllers).length} interactive).`,
);
